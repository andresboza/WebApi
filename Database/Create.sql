CREATE TABLE POSITIONS (
  ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TITLE         VARCHAR2(100)  NOT NULL,
  DESCRIPTION   VARCHAR2(1000) NOT NULL,
  LOCATION      VARCHAR2(255)  NOT NULL,
  STATUS        VARCHAR2(20)   NOT NULL CHECK (STATUS IN ('draft','open','closed','archived')),
  RECRUITER_ID  NUMBER         NOT NULL,
  DEPARTMENT_ID NUMBER         NOT NULL,
  BUDGET        NUMBER(15,2)   NOT NULL,
  CLOSING_DATE  DATE
);

COMMENT ON COLUMN POSITIONS.TITLE        IS 'Título del puesto (máx. 100 caracteres)';
COMMENT ON COLUMN POSITIONS.DESCRIPTION  IS 'Descripción detallada (máx. 1000 caracteres)';
COMMENT ON COLUMN POSITIONS.LOCATION     IS 'Ubicación del puesto';
COMMENT ON COLUMN POSITIONS.STATUS       IS 'Estado: draft, open, closed, archived';
COMMENT ON COLUMN POSITIONS.RECRUITER_ID IS 'ID del reclutador';
COMMENT ON COLUMN POSITIONS.DEPARTMENT_IDIS 'ID del departamento';
COMMENT ON COLUMN POSITIONS.BUDGET       IS 'Presupuesto asignado';
COMMENT ON COLUMN POSITIONS.CLOSING_DATE IS 'Fecha límite para aplicar';

select * from positions